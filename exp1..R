print("---- Without R Objects ----")
print(5 + 9)
print(5 - 9)
print(5 * 9)
print(5 / 9)

print("---- With R Objects ----")
a <- 5
b <- 9

print("a =")
print(a)
print("b =")
print(b)

print("Addition (a + b):")
print(a + b)

print("Subtraction (a - b):")
print(a - b)

print("Multiplication (a * b):")
print(a * b)

print("Division (a / b):")
print(a / b)

print("---- Mathematical Functions ----")
print("Square root of 25:")
print(sqrt(25))

print("Natural log of 10:")
print(log(10))

print("Exponential of 2:")
print(exp(2))

print("Sine of pi/2:")
print(sin(pi / 2))

print("Cosine of 0:")
print(cos(0))
